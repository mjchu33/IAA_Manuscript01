def decompose_NMF3(dpv_datagroup, abundances, endmembers, volts):
    # decompose data

    component_arrays_concatenated_all = np.empty((np.shape(dpv_datagroup)[0], np.shape(dpv_datagroup)[1] * 4))
    for i in range(0,np.shape(dpv_datagroup)[0]):
        fig = plt.figure(1)
        fig.set_figheight(20)
        fig.set_figwidth(15)

        plt.subplot(15,15,i+1)
        e1 = abundances[i,0] * endmembers[0,:]
        e2 = abundances[i,1] * endmembers[1,:]
        e3 = abundances[i,2] * endmembers[2,:]

        plt.plot(volts[0,:], dpv_datagroup[i,:], 'k')
        plt.plot(volts[0,:], e1)
        plt.plot(volts[0,:], e2)
        plt.plot(volts[0,:], e3)

        plt.ylabel('current (A)', fontsize=5)
        plt.xlabel('voltage (V vs. Ag/AgCl)', fontsize=5)
        plt.xticks(fontsize = 5)
        plt.yticks(fontsize = 5)

        rawdata = dpv_datagroup[i,:]

        # horizontally concatenates all the data; will become a 1D vector
        component_arrays = [rawdata.T, e1.T, e2.T, e3.T]
        component_arrays_concatenated1 = np.hstack((rawdata.T, e1.T))
        component_arrays_concatenated2 = np.hstack((component_arrays_concatenated1, e2.T))
        component_arrays_concatenated3 = np.hstack((component_arrays_concatenated2, e3.T))

        component_arrays_concatenated3 = np.reshape(component_arrays_concatenated3, (1, np.shape(component_arrays_concatenated3)[0]))
        component_arrays_concatenated_all[i,:] = np.float32((component_arrays_concatenated3))

    return component_arrays, component_arrays_concatenated_all

