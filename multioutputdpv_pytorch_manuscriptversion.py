def normalize_data_to_max(dpv_datagroup_withheaders):
    volts = np.float32(dpv_datagroup_withheaders[0,:])  # extract volts from first row of input data
    dpv_datagroup_normalized = np.empty(np.shape(dpv_datagroup_withheaders))  # make empty array
    dpv_datagroup_normalized[0,:] = volts  # set first row of the new dataset to volts

    for i in range(1, np.shape(dpv_datagroup_withheaders)[0]):
        dpv_datagroup_normalized[i,:] = np.float32( dpv_datagroup_withheaders[i,:] / np.max(dpv_datagroup_withheaders[i,:]) )

        plt.figure(1)
        plt.plot(volts, dpv_datagroup_normalized[i,:])
        plt.xlabel('V vs Ag/AgCl')
        plt.ylabel('Normalized Current (i/i_max)')

    return dpv_datagroup_normalized

def NMF_process(data, num_endmembers):
    import matplotlib
    import matplotlib.pyplot as plt
    import seaborn as sns
    from sklearn.decomposition import NMF
    from random import randint

    # shave off the first row (attributed to voltage values)
    ydata = data[1:, :]

    # use NMF
    model = NMF(n_components = num_endmembers, init='random', random_state=randint(0, 1000), max_iter = 1000)
    endmembers = model.fit(ydata).components_ # X in our equation
    abundances = model.transform(ydata) # beta in our equation

    # determine shape of data
    dims = np.shape(ydata)

    # call out voltage (first row)
    volts = np.float32(data[0,:])

    # plot the results
    plt.figure(1)
    v = np.reshape(volts, (1, dims[1]))
    plt.plot(v.T, endmembers.T)
    plt.xlabel('V vs Ag/AgCl')
    #labels = ['E1','E2']
    #plt.legend(labels)

    # Run these steps a few times until you see a significant change.
    # The endmembers change on each run!

    #%% plot endmembers
    plt.figure(2)
    plt.clf()

    for i in range(0, num_endmembers):
        plt.subplot(2,2,i+1)
        plt.plot(v[0,:], endmembers[i,:])
        plt.xlabel('V vs Ag/AgCl')
        plt.title('E' + str(i+1))
        plt.tight_layout()

    return model, abundances, endmembers, v

