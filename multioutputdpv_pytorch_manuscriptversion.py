# remove outliers, take data only within 1 std dev from mean
def remove_outliers_zscore(data, threshold):
    mean = np.mean(data)
    std = np.std(data)
    z_scores = (data - mean) / std
    return data[np.abs(z_scores) < threshold]

def remove_outliers_zscore_median(data, threshold):
    median = np.median(data)
    std = np.std(data)
    z_scores = (data - median) / std
    return data[np.abs(z_scores) < threshold]

